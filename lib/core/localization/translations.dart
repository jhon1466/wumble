import 'package:wumble/core/localization/locale_controller.dart';

/// Source-keyed translation: the Spanish text IS the key.
///
/// Usage anywhere (no BuildContext needed): `Text(tr('Cancelar'))`.
/// - Spanish (native) returns the source string unchanged.
/// - English/Russian look up [_en] / [_ru]; if missing, fall back to Spanish.
///
/// Add entries to [_en] and [_ru] as screens are migrated.
String tr(String es) {
  switch (LocaleController.locale.value.languageCode) {
    case 'en':
      return _en[es] ?? es;
    case 'ru':
      return _ru[es] ?? es;
    default:
      return es;
  }
}

